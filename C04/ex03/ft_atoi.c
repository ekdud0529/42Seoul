int	ft_atoi(char	*str)
{
	int	sign;
	int	num;

	num = 0;
	sign = 1;
	while (*str)
	{
		if (*str == '-')
			sign *= -1;
		if (*str != ' ' && *str != '+' && *str != '-'
			&& ('0' > *str || '9' < *str))
			break ;
		if ('0' <= *str && *str <= '9')
			num = num * 10 + (*str - '0');
		str++;
	}
	num *= sign;
	return (num);
}
