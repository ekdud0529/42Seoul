char	*ft_strcapitalize(char	*str)
{
	char	*cmp;

	if (97 <= *str && *str <= 122)
	{
		*str = *str - 32;
		str++;
	}
	while (*str)
	{
		if (97 <= *str && *str <= 122)
		{
			cmp = str - 1;
			if ((32 <= *cmp && *cmp <= 47)
				|| (58 <= *cmp && *cmp <= 64)
				|| (91 <= *cmp && *cmp <= 96)
				|| (123 <= *cmp && *cmp <= 126))
			{
				*str = *str - 32;
			}
		}
		str++;
	}
	return (str);
}
