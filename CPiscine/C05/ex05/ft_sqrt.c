int	ft_sqrt(int	nb)
{
	long long	sqrt;

	sqrt = 1;
	while (nb > 0 && sqrt * sqrt <= nb)
	{
		if (sqrt * sqrt == nb)
			return (sqrt);
		sqrt++;
	}
	return (0);
}
